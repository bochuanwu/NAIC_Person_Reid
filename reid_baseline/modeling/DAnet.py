        if self.neck == 'no':
            feat = global_feat
        elif self.neck == 'bnneck':
            feat = self.bottleneck(global_feat)  # normalize for angular softmax
            feat2 = self.bottleneck2(attention_feat)

        if self.training:
            cls_score = self.classifier(feat)
            cls_score2 = self.classifier2(feat2)
